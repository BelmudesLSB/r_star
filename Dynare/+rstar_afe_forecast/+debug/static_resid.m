function [lhs, rhs] = static_resid(y, x, params)
T = NaN(4, 1);
lhs = NaN(16, 1);
rhs = NaN(16, 1);
T(1) = exp(params(11)/400);
T(2) = T(1)/(T(1)-params(1)*params(4))*T(1)/(T(1)-params(4));
T(3) = params(4)/T(1);
T(4) = T(2)^(-1);
lhs(1) = y(14);
rhs(1) = params(11)+400*y(8);
lhs(2) = y(15);
rhs(2) = params(9)+400*y(4);
lhs(3) = y(16);
rhs(3) = params(9)+params(10)+400*y(6);
lhs(4) = y(1);
rhs(4) = y(1)-(y(6)-y(4)-y(12))*T(4);
lhs(5) = y(10);
rhs(5) = y(1)+T(3)*(y(2)-y(11))+params(1)*T(3)*(y(10)-T(3)*y(10));
lhs(6) = y(5);
rhs(6) = y(6)-y(4);
lhs(7) = y(3);
rhs(7) = y(7)+params(1)*y(3)+params(3)*(y(10)*params(2)+T(2)*y(1));
lhs(8) = y(4);
rhs(8) = y(3)+y(4)*params(5);
lhs(9) = y(10);
rhs(9) = y(2)-y(11);
lhs(10) = y(12);
rhs(10) = y(8)+y(9);
lhs(11) = y(13);
rhs(11) = y(4)+y(12);
lhs(12) = y(11)*params(2)+T(2)*(y(11)-T(3)*y(11))-(y(11)-T(3)*y(11))*T(3)*params(1)*T(2);
rhs(12) = (params(1)*y(8)-y(8))*T(2)*T(3)+y(9)*params(1)*T(3)/(1-params(1)*T(3));
lhs(13) = y(6);
rhs(13) = y(6)*params(6)+(1-params(6))*(y(4)*params(7)+y(10)*params(8)/4)+params(18)/400*x(1);
lhs(14) = y(9);
rhs(14) = y(9)*params(12)+params(15)/400*x(3);
lhs(15) = y(8);
rhs(15) = y(8)*params(13)+params(16)/400*x(4);
lhs(16) = y(7);
rhs(16) = y(7)*params(14)+params(17)/400*x(2);
end
